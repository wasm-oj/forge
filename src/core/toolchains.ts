/** Exact executable/compiler content used by cache keys and cost calibration. */
export function toolchainContentIdentity(language: Language): string {
  if (!isBuiltinLanguage(language)) throw new Error(`Forge has no built-in toolchain for '${language}'.`);
  return TOOLCHAIN_CONTENT_SHA256[language].join(".");
}

export function expectedToolchainAssetSha256(assetPath: string): string {
  const normalized = assetPath.startsWith("/") ? assetPath : `/toolchains/${assetPath}`;
  const expected = PINNED_TOOLCHAIN_ASSET_SHA256[normalized];
  if (!expected) throw new Error(`No pinned digest is declared for toolchain asset '${assetPath}'.`);
  return expected;
}

/**
 * Resolves an immutable browser asset request. The digest query is part of the
 * HTTP and Cache Storage key, so rebuilding a same-version asset cannot return
 * bytes cached under an older content identity.
 */
export function contentAddressedToolchainAssetUrl(assetPath: string, assetBaseUrl: URL): URL {
  const sha256 = expectedToolchainAssetSha256(assetPath);
  const base = new URL(assetBaseUrl.href);
  if (!base.pathname.endsWith("/")) base.pathname += "/";
  const inheritedSearch = [...base.searchParams];
  base.search = "";
  const filename = assetPath.slice(assetPath.lastIndexOf("/") + 1);
  const url = new URL(filename, base);
  for (const [name, value] of inheritedSearch) url.searchParams.append(name, value);
  url.searchParams.set("sha256", sha256);
  return url;
}

function freezeToolchain(definition: ToolchainDefinition): Readonly<ToolchainDefinition> {
  return Object.freeze({
    ...definition,
    compilerPackages: Object.freeze([...definition.compilerPackages]),
    targets: Object.freeze([...definition.targets]),
  });
}

