export function toolchainCacheIdentity(language: Language) {
  if (!isBuiltinLanguage(language)) throw new Error(`Forge has no built-in toolchain for '${language}'.`);
  const toolchain = TOOLCHAINS[language];
  return {
    forgeContract: FORGE_CONTRACT_VERSION,
    version: toolchain.version,
    compilerPackages: [...toolchain.compilerPackages],
    runtimePackage: toolchain.runtimePackage,
    contentSha256: [...TOOLCHAIN_CONTENT_SHA256[language]],
  };
}

export function toolchainPackageIdentities(language: Language): string[] {
  if (!isBuiltinLanguage(language)) throw new Error(`Forge has no built-in toolchain for '${language}'.`);
  const toolchain = TOOLCHAINS[language];
  return [...new Set([
    ...toolchain.compilerPackages,
    ...(toolchain.runtimePackage ? [toolchain.runtimePackage] : []),
  ])];
}

export function extensionLanguage(path: string): Language | undefined {
  const extension = path.slice(path.lastIndexOf(".") + 1).toLowerCase();
  return ({
    c: "c",
    h: "c",
    cc: "cpp",
    cpp: "cpp",
    cxx: "cpp",
    hpp: "cpp",
    rs: "rust",
    py: "python",
    js: "javascript",
    ts: "typescript",
    go: "go",
  } as Record<string, Language | undefined>)[extension];
}

export function languageLabel(language: Language): string {
  if (!isBuiltinLanguage(language)) return language;
  return ({ c: "C", cpp: "C++", rust: "Rust", python: "Python", javascript: "JavaScript", typescript: "TypeScript", go: "Go" })[language];
}
