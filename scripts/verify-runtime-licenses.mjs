import { execFile } from "node:child_process";
import { createHash } from "node:crypto";
import { readFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { promisify } from "node:util";

const run = promisify(execFile);
const root = path.resolve(fileURLToPath(new URL("..", import.meta.url)));
const inventoryPath = path.join(root, "licenses/runtime-core-dependencies.json");
const reportPath = path.join(root, "licenses/runtime-core-dependencies.html");
const [inventoryBytes, reportBytes, tree] = await Promise.all([
  readFile(inventoryPath),
  readFile(reportPath),
  run("cargo", [
    "tree",
    "--locked",
    "--manifest-path", path.join(root, "crates/runtime-core/Cargo.toml"),
    "--target", "wasm32-unknown-unknown",
    "--no-default-features",
    "--features", "web",
    "--edges", "normal",
    "--prefix", "none",
    "--format", "{p}",
  ], { cwd: root, maxBuffer: 16 * 1024 * 1024 }),
]);
const inventory = JSON.parse(inventoryBytes.toString("utf8"));
if (inventory.schema !== "wasm-oj-forge-v1/runtime-core-licenses") {
  throw new Error(`Unsupported runtime license inventory schema '${String(inventory.schema)}'.`);
}
if (inventory.generator?.name !== "cargo-about" || inventory.generator?.version !== "0.9.1") {
  throw new Error("The runtime license inventory was not generated by pinned cargo-about 0.9.1.");
}
const expectedGraph = {
  manifest: "crates/runtime-core/Cargo.toml",
  lockfile: "crates/runtime-core/Cargo.lock",
  target: "wasm32-unknown-unknown",
  defaultFeatures: false,
  features: ["web"],
  dependencyKinds: ["normal"],
};
if (JSON.stringify(inventory.graph) !== JSON.stringify(expectedGraph)) {
  throw new Error("The runtime license inventory describes a non-canonical dependency graph.");
}
const reportDigest = createHash("sha256").update(reportBytes).digest("hex");
if (
  inventory.report?.path !== "licenses/runtime-core-dependencies.html"
  || inventory.report?.sha256 !== reportDigest
) {
  throw new Error("The runtime license inventory does not bind the distributed HTML report.");
}

const actualPackages = new Set(
  tree.stdout
    .split("\n")
    .map((line) => /^(\S+) v([^\s]+)/.exec(line))
    .filter(Boolean)
    .map((match) => `${match[1]}@${match[2]}`),
);
actualPackages.delete("wasm-oj-forge-runtime-core@0.1.0");
const inventoryPackages = new Set();
for (const item of inventory.packages ?? []) {
  if (
    typeof item?.name !== "string"
    || typeof item.version !== "string"
    || (item.repository !== null && typeof item.repository !== "string")
    || !Array.isArray(item.selectedLicenses)
    || item.selectedLicenses.length === 0
    || item.selectedLicenses.some((license) => typeof license !== "string" || !license)
  ) {
    throw new Error("The runtime license inventory contains an invalid package record.");
  }
  const identity = `${item.name}@${item.version}`;
  if (inventoryPackages.has(identity)) throw new Error(`Duplicate runtime license package '${identity}'.`);
  inventoryPackages.add(identity);
  if (!reportBytes.includes(Buffer.from(`${item.name} ${item.version}`))) {
    throw new Error(`Runtime license report omits '${identity}'.`);
  }
}

const missing = [...actualPackages].filter((value) => !inventoryPackages.has(value)).sort();
const unexpected = [...inventoryPackages].filter((value) => !actualPackages.has(value)).sort();
if (missing.length > 0 || unexpected.length > 0) {
  throw new Error(
    "Runtime license inventory differs from the locked web dependency graph."
    + `${missing.length > 0 ? ` Missing: ${missing.join(", ")}.` : ""}`
    + `${unexpected.length > 0 ? ` Unexpected: ${unexpected.join(", ")}.` : ""}`,
  );
}
if (inventoryPackages.size < 1 || reportBytes.byteLength < 1_024) {
  throw new Error("Runtime dependency license report is unexpectedly empty.");
}
process.stdout.write(`Verified ${inventoryPackages.size} runtime dependency license records.\n`);
